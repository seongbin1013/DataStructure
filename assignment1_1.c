#include <stdio.h>

int main(void) {
    // 0을 비트 반전(NOT 연산)시킵니다.
    // 0은 모든 비트가 0000...0 이고, 이를 ~ 연산하면 1111...1 이 됩니다.
    int test_val = ~0; // ~은 NOT 연산자로, 모든 비트를 반전시킵니다.

    printf("0을 비트 반전한 값(~0) : %d\n", test_val);

    // 2의 보수 시스템에서는 모든 비트가 1인 상태(1111...1)가 -1을 의미합니다.
    if (test_val == -1) {
        printf("현재 시스템은 [2의 보수] 방식을 사용.\n");
    } 
    // 1의 보수 시스템에서는 모든 비트가 1인 상태가 -0을 의미할 수 있습니다.
    else {
        printf("현재 시스템은 [1의 보수] (또는 부호와 절댓값) 방식을 사용.\n");
    }

    return 0;
}